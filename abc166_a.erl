-module('Main').
-export([main/1]).

main(_) ->
    {ok, [S]} = io:fread("", "~s"),
    case string:equal(S, "ABC") of
        true ->
            io:format("ARC~n");
        false ->
            io:format("ABC~n")
    end,
    halt().
