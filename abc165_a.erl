-module('Main').
-export([main/1]).

main(_) ->
    {ok, [K]} = io:fread("", "~d"),
    {ok, [A, B]} = io:fread("", "~d ~d"),
    Ans = check_divisible(A, B, K),
    io:format("~s~n", Ans),
    halt().

check_divisible(A, B, K) ->
    Ans = lists:foldl(fun(X, Acc) ->
                          case X rem K of
                              0 -> 'OK';
                              _ -> Acc
                          end
                      end, 'NG', lists:seq(A, B)),
    Ans.
